# PENGGUNAAN FOR LOOP
#1. For untuk List
nama_hewan = ['kucing', 'tikus', 'jerapah', 'gajah']
for hewan in nama_hewan :
    print(hewan)

print ('=' * 15)

#2. For untuk Fungsi Range
for i in range(5,10,2):
    print(i)

print ('=' * 15)

#3. For dengan String
for karakter in 'jerapah':
    print(karakter)